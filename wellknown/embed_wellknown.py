#!/usr/bin/env python3
"""Embed the vendored well-known-type .proto sources into a generated C++ TU.

Reads every wellknown/*.proto and writes src/wellknown_generated.cpp, which defines
rapidproto::wellknown_source() (declared in include/rapidproto/wellknown.hpp). The
generated file is checked in; re-run this script whenever the vendored sources change:

    python3 wellknown/embed_wellknown.py
"""

import pathlib
import sys

REPO = pathlib.Path(__file__).resolve().parent.parent
SRC_DIR = REPO / "wellknown"
OUT = REPO / "src" / "wellknown_generated.cpp"
DELIM = "RPWKT"  # raw-string delimiter; must not appear as )RPWKT" in any source
IMPORT_PREFIX = "google/protobuf/"


def main() -> None:
    protos = sorted(SRC_DIR.glob("*.proto"))
    if not protos:
        sys.exit("embed_wellknown: no .proto files in wellknown/")

    out = [
        "// GENERATED by wellknown/embed_wellknown.py -- do not edit.\n",
        "// Sources are the vendored well-known types under wellknown/ (BSD-3-Clause).\n\n",
        '#include "rapidproto/wellknown.hpp"\n\n',
        "#include <optional>\n",
        "#include <string_view>\n\n",
        "namespace rapidproto {\n",
        "namespace {\n\n",
    ]
    entries = []
    for i, proto in enumerate(protos):
        text = proto.read_text(encoding="utf-8")
        if f"){DELIM}\"" in text:
            sys.exit(f"embed_wellknown: raw-string delimiter collision in {proto.name}")
        var = f"kWkt{i}"
        entries.append((IMPORT_PREFIX + proto.name, var))
        out.append(f"constexpr std::string_view {var} =\n")
        out.append(f'    R"{DELIM}({text}){DELIM}";\n\n')

    out.append("}  // namespace\n\n")
    out.append(
        "std::optional<std::string_view> wellknown_source(std::string_view import_path) {\n"
    )
    for import_path, var in entries:
        out.append(f'    if (import_path == "{import_path}") {{\n')
        out.append(f"        return {var};\n")
        out.append("    }\n")
    out.append("    return std::nullopt;\n")
    out.append("}\n\n")
    out.append("}  // namespace rapidproto\n")

    OUT.write_text("".join(out), encoding="utf-8")
    print(f"embed_wellknown: wrote {OUT.relative_to(REPO)} ({len(protos)} types)")


if __name__ == "__main__":
    main()
